function getReviewCategoryItems(filterName) {
  const categoryNames = filterName === "all"
    ? ["tops", "couple", "family", "ocean", "hanok"]
    : [filterName];

  return categoryNames.flatMap((categoryName, categoryIndex) => {
    const category = reviewCategoryData[categoryName];
    if (!category) return [];

    const items = category.names.map((name, index) => {
      const sourceReview = reviewItems[category.images[index] % reviewItems.length];
      return {
        name,
        image: sourceReview.image,
        alt: sourceReview.alt,
        content: reviewSummary,
        tags: category.tags[index],
        category: categoryName,
      };
    });

    return filterName === "all"
      ? [items[categoryIndex % items.length]]
      : items;
  });
}

